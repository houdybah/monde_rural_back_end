package mae.cna.di.monde_rural.dto;


public class RaceFamilleDto {
	
	private String uuid;
	
	private String code;
	
	private String libelle;
	
	private String description;
	
	private String especeUuid;
	
	private String especeLibelle;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getLibelle() {
		return libelle;
	}

	public void setLibelle(String libelle) {
		this.libelle = libelle;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getEspeceUuid() {
		return especeUuid;
	}

	public void setEspeceUuid(String especeUuid) {
		this.especeUuid = especeUuid;
	}

	public String getEspeceLibelle() {
		return especeLibelle;
	}

	public void setEspeceLibelle(String especeLibelle) {
		this.especeLibelle = especeLibelle;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	
	
	
}