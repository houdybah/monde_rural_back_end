package mae.cna.di.monde_rural.dto;

public class VillageSecteurDto {
	
	private String uuid;
	
	private String code;
	
	private String libelle;
	
	private String description;
	
	private String districtQuartierUuid;
	
	private String districtQuartierLibelle;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getLibelle() {
		return libelle;
	}

	public void setLibelle(String libelle) {
		this.libelle = libelle;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getDistrictQuartierUuid() {
		return districtQuartierUuid;
	}

	public void setDistrictQuartierUuid(String districtQuartierUuid) {
		this.districtQuartierUuid = districtQuartierUuid;
	}

	public String getDistrictQuartierLibelle() {
		return districtQuartierLibelle;
	}

	public void setDistrictQuartierLibelle(String districtQuartierLibelle) {
		this.districtQuartierLibelle = districtQuartierLibelle;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	
	
}
