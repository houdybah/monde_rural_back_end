package mae.cna.di.monde_rural.dto;

public class SousPrefectureDto {
	
	private String uuid;
	
	private String code;
	
	private String libelle;
	
	private String description;
	
	private String prefectureUuid;
	
	private String prefectureLibelle;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getLibelle() {
		return libelle;
	}

	public void setLibelle(String libelle) {
		this.libelle = libelle;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getPrefectureUuid() {
		return prefectureUuid;
	}

	public void setPrefectureUuid(String prefectureUuid) {
		this.prefectureUuid = prefectureUuid;
	}

	public String getPrefectureLibelle() {
		return prefectureLibelle;
	}

	public void setPrefectureLibelle(String prefectureLibelle) {
		this.prefectureLibelle = prefectureLibelle;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	
	

	
}
